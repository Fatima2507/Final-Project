        public override string[] GetRolesForUser(string username)
        {
            using (var details = new stationary_managementEntities())
            {
                var u = (from user_reg in details.user_reg
                         join emp_roles in details.emp_roles on user_reg.id
                         equals emp_roles.emp_no
                         where user_reg.fname == username
                         select emp_roles.roles).ToArray();

                return u;
            }

